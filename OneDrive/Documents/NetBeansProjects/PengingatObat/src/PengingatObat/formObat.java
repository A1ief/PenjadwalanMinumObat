    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String jumlah = jumlahField.getText();
        String nama = namaField.getText();
        String jadwal = jadwalField.getText();

        if (resepButton.isSelected()) {
            // Obat Resep
            String dokter = dokterField.getText();
            String resep = resepField.getText();
            
            daftarObat.add(new ObatResep(nama, jadwal, dokter, resep, jumlah)); // Menambahkan obat resep
        } else if (bebasButton.isSelected()) {
            // Obat Bebas
            String kategori = kategoriField.getText();
            daftarObat.add(new ObatBebas(nama, jadwal, kategori, jumlah)); // Menambahkan obat bebas
        }

        // Menampilkan daftar obat yang telah ditambahkan
        tampilkanDaftarObat();

        // Mengosongkan field setelah menambah obat
        jumlahField.setText("");
        namaField.setText("");
        jadwalField.setText("");
        dokterField.setText("");
        resepField.setText("");
        kategoriField.setText("");
    }//GEN-LAST:event_addButtonActionPerformed

