    // Fungsi untuk menangani jenis obat yang dipilih
    private void handleObatTypeSelection() {
        if (resepButton.isSelected()) {
            dokterField.setVisible(true); // Tampilkan nama dokter
            resepField.setVisible(true);  // Tampilkan nomor resep
            kategoriField.setVisible(false); // Sembunyikan kategori obat
        } else if (bebasButton.isSelected()) {
            kategoriField.setVisible(true); // Tampilkan kategori obat
            dokterField.setVisible(false); // Sembunyikan nama dokter
            resepField.setVisible(false);  // Sembunyikan nomor resep
        }
    }

