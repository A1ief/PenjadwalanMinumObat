    // Fungsi untuk menampilkan daftar obat pada text area
    private void tampilkanDaftarObat() {
        displayArea.setText(""); // Kosongkan area display
        for (Obat obat : daftarObat) {
            displayArea.append(obat.toString() + "\n"); // Menampilkan daftar obat
        }
    }

    // Kelas abstrak Obat
    public abstract class Obat {

        protected String nama;
        protected String jadwal;
        protected String jumlah;

        public Obat(String nama, String jadwal, String jumlah) {
            this.nama = nama;
            this.jadwal = jadwal;
            this.jumlah = jumlah;
        }

        @Override
        public abstract String toString();
    }

    // Kelas untuk Obat Resep
    public class ObatResep extends Obat {

        private String dokter;
        private String resep;

        public ObatResep(String nama, String jadwal, String dokter, String resep, String jumlah) {
            super(nama, jadwal, jumlah);
            this.dokter = dokter;
            this.resep = resep;
        }

        @Override
        public String toString() {
            return "Jumlah Obat : " + jumlah + "\n" 
                    + "Nama Obat : " + nama + "\n" 
                    + "Jadwal : " + jadwal + "\n" 
                    + "Dokter : " + dokter + "\n" 
                    + "No Resep : " + resep + "\n"
                    + "=========================";
        }
    }

    // Kelas untuk Obat Bebas
    public class ObatBebas extends Obat {

        private String kategori;

        public ObatBebas(String nama, String jadwal, String kategori, String jumlah) {
            super(nama, jadwal, jumlah);
            this.kategori = kategori;
        }

        @Override
        public String toString() {
            return "Jumlah Obat : " + jumlah + "\n" 
                    + "Nama Obat : " + nama + "\n" 
                    + "Jadwal : " + jadwal + "\n" 
                    + "Kategori : " + kategori + "\n"
                    + "=========================";
        }
    }

